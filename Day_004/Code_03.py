# program to clear the concept of escape sequence
print("Hello World!\nWelcome to Python programming.\nLet's learn together!")
print("This is an backslash : \\")
print("This is an single quote : '")
print("This is an double quote : \"")
print("This is an tab space : \t")
print("This is an backspace : \b")
print("This is an carriage return : \r")
print("This is an form feed : \f")
print("This is an vertical tab : \v")
print("This is an octal value : \101")  # prints 'A'
print("This is an hexadecimal value : \x41")  # prints 'A'